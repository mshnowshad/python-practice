

"""Python Casting"""
#>>> python casting means changing the 'Data type' of a variable from one type to another type
print("Python Casting")
print()




print("Example of 'int' casting")

inte1 = int(1)   # x will be 1
inte2 = int(2.8) # y will be 2
inte3 = int("3") # z will be 3

print("same as like:",type(inte1))
print("same as like:",type(inte2))
print("same as like:",type(inte3))


print()

print("Example of 'float' casting")

float1 = float(1)     # float1 will be 1.0
float2 = float(2.8)   # float2 will be 2.8
float3 = float("3")   # float3 will be 3.0
float4 = float("4.2") # float4 will be 4.2

print("same as like:",type(float1))
print("same as like:",type(float2))
print("same as like:",type(float3))
print("same as like:",type(float4))
print()



print("Example of 'str' casting:")

str1 = str("s1") # x will be 's1'
str2 = str(2)    # y will be '2'
str3 = str(3.0)  # z will be '3.0'

print("same as like:",type(str1))
print("same as like:",type(str2))
print("same as like:",type(str3))


print()













































